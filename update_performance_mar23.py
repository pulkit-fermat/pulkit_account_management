#!/usr/bin/env python3
"""
Update performance data for all 12 brands through Mar 23, 2026.
All data from FERMAT Platform MCP get_funnel_metrics API.
Run: python3 update_performance_mar23.py
"""
import json, os
from datetime import date

DIR = os.path.dirname(os.path.abspath(__file__))
DATA = os.path.join(DIR, "data")
LAST_UPDATED = "2026-03-24T10:30:00Z"

PERIODS = {
    "yesterday": (date(2026,3,23), date(2026,3,23)),
    "3d":        (date(2026,3,21), date(2026,3,23)),
    "7d":        (date(2026,3,17), date(2026,3,23)),
    "15d":       (date(2026,3,9),  date(2026,3,23)),
    "30d":       (date(2026,2,22), date(2026,3,23)),
    "45d":       (date(2026,2,7),  date(2026,3,23)),
}

def agg(daily):
    perf = {}
    for pn, (s, e) in PERIODS.items():
        ts=tr=tp=tv=ta=tc=0
        for r in daily:
            d = date.fromisoformat(r["date"])
            if s <= d <= e:
                ts+=r["sessions"]; tr+=r["revenue"]; tp+=r["purchases"]
                tv+=r["pdp_views"]; ta+=r["atc"]; tc+=r["checkouts"]
        perf[pn] = {
            "ad_spend":None, "revenue":round(tr,2), "cvr":round(tp/ts*100,4) if ts else 0,
            "sessions":ts, "roas":None, "cpa":None, "purchases":tp,
            "aov":round(tr/tp,2) if tp else 0, "pdp_views":tv, "atc":ta, "checkouts":tc
        }
    return perf

def upd(cid, daily, label):
    fp = os.path.join(DATA, f"{cid}.json")
    if not os.path.exists(fp):
        print(f"  SKIP {label}: file not found"); return
    with open(fp) as f: data = json.load(f)
    perf = agg(daily)
    perf["daily"] = daily
    data["performance"] = perf
    data["last_updated"] = LAST_UPDATED
    with open(fp,"w") as f: json.dump(data, f, indent=2, ensure_ascii=False)
    y=perf["yesterday"]; d3=perf["30d"]
    print(f"  {label:<22} yesterday: {y['sessions']:>6}s ${y['revenue']:>10,.2f} {y['purchases']:>3}p | 30d: {d3['sessions']:>7}s ${d3['revenue']:>12,.2f} {d3['purchases']:>4}p")

# Compact: d=date s=sessions r=revenue p=purchases v=pdp_views a=atc c=checkouts
def R(d,s,r,p,v,a,c): return {"date":d,"sessions":s,"revenue":round(r,2),"purchases":p,"pdp_views":v,"atc":a,"checkouts":c}

