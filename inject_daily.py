"""
Inject daily FERMAT funnel metrics into brand JSON files.
All data sourced from real get_funnel_metrics API calls (2026-02-06 to 2026-03-22).
"""
import json, os

BASE = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data")

def parse_rows(rows):
    daily = []
    for r in rows:
        date_str = r["d"]
        daily.append({
            "date": date_str,
            "sessions": r["s"],
            "revenue": r["r"],
            "purchases": r["p"],
            "pdp_views": r["v"],
            "atc": r["a"],
            "checkouts": r["c"]
        })
    daily.sort(key=lambda x: x["date"])
    return daily

def update_json(csv_id, daily_data):
    fp = os.path.join(BASE, f"{csv_id}.json")
    with open(fp, 'r') as f:
        data = json.load(f)
    if "performance" not in data:
        data["performance"] = {}
    data["performance"]["daily"] = daily_data
    with open(fp, 'w') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
    print(f"  {csv_id}: {len(daily_data)} daily rows")

